	private int[][] map;

	private BufferedImage[][] imagemap;

	public Map(MAPS map)
	{
		tilesize = 70;

		dimensions = new Dimension(28, 16);

		tile = new Point();

		imagemap = new BufferedImage[dimensions.height][dimensions.width];

		switch (map)
		{
			case BLANK:
				this.map = blank;
				break;
			default:
				break;
		}

		for (int i = 0; i < dimensions.height; ++i)
		{
			for (int j = 0; j < dimensions.width; ++j)
			{
				switch (this.map[i][j])
				{
					case 0: // Clear Tile.
						imagemap[i][j] = null;
						break;
					case 1: // Square Grass Tile.
						tile.x = 504;
						tile.y = 576;
						break;
					case 2: // Square Dirt Tile.
						tile.x = 576;
						tile.y = 864;
						break;
					default:
						break;
				}
				
				if (this.map[i][j] != 0)
				{
					imagemap[i][j] = Game.tilesSpriteSheet.getSprite(tile.x, tile.y, tilesize);
				}
			}
		}
	}

	public void paint(Graphics g2d)
	{
		for (int i = 0; i < dimensions.height; ++i)
		{
			for (int j = 0; j < dimensions.width; ++j)
			{
				g2d.drawImage(imagemap[i][j], j * tilesize, i * tilesize, null);
			}
		}
	}

	public int[][] getMap()
	{
		return map;
	}
